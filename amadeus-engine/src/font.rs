//! Built-in "Hacker Terminal" Font Data
//! A very simple 5x7 monospace bitmap font for the Amadeus console.

/// Returns the pixel data for a given ASCII character.
/// Each character is a 5x7 grid of pixels, represented as a flat array of 35 booleans.
pub fn get_char_data(c: char) -> [bool; 35] {
    let bitmap: [u8; 7] = match c {
        'A' | 'a' => [0x20, 0x50, 0x88, 0x88, 0xF8, 0x88, 0x88],
        'B' | 'b' => [0xF0, 0x48, 0x48, 0x70, 0x48, 0x48, 0xF0],
        'C' | 'c' => [0x70, 0x88, 0x80, 0x80, 0x80, 0x88, 0x70],
        'D' | 'd' => [0xF0, 0x48, 0x48, 0x48, 0x48, 0x48, 0xF0],
        'E' | 'e' => [0xF8, 0x80, 0x80, 0xF0, 0x80, 0x80, 0xF8],
        'F' | 'f' => [0xF8, 0x80, 0x80, 0xF0, 0x80, 0x80, 0x80],
        'G' | 'g' => [0x78, 0x80, 0x80, 0xB8, 0x88, 0x88, 0x78],
        'H' | 'h' => [0x88, 0x88, 0x88, 0xF8, 0x88, 0x88, 0x88],
        'I' | 'i' => [0x70, 0x20, 0x20, 0x20, 0x20, 0x20, 0x70],
        'J' | 'j' => [0x38, 0x10, 0x10, 0x10, 0x10, 0x90, 0x60],
        'K' | 'k' => [0x88, 0x90, 0xA0, 0xC0, 0xA0, 0x90, 0x88],
        'L' | 'l' => [0x80, 0x80, 0x80, 0x80, 0x80, 0x80, 0xF8],
        'M' | 'm' => [0x88, 0xD8, 0xA8, 0xA8, 0x88, 0x88, 0x88],
        'N' | 'n' => [0x88, 0xC8, 0xA8, 0x98, 0x88, 0x88, 0x88],
        'O' | 'o' => [0x70, 0x88, 0x88, 0x88, 0x88, 0x88, 0x70],
        'P' | 'p' => [0xF0, 0x88, 0x88, 0xF0, 0x80, 0x80, 0x80],
        'Q' | 'q' => [0x70, 0x88, 0x88, 0x88, 0xA8, 0x90, 0x68],
        'R' | 'r' => [0xF0, 0x88, 0x88, 0xF0, 0xA0, 0x90, 0x88],
        'S' | 's' => [0x78, 0x80, 0x80, 0x70, 0x08, 0x08, 0xF0],
        'T' | 't' => [0xF8, 0x20, 0x20, 0x20, 0x20, 0x20, 0x20],
        'U' | 'u' => [0x88, 0x88, 0x88, 0x88, 0x88, 0x88, 0x70],
        'V' | 'v' => [0x88, 0x88, 0x88, 0x88, 0x88, 0x50, 0x20],
        'W' | 'w' => [0x88, 0x88, 0x88, 0xA8, 0xA8, 0xD8, 0x88],
        'X' | 'x' => [0x88, 0x88, 0x50, 0x20, 0x50, 0x88, 0x88],
        'Y' | 'y' => [0x88, 0x88, 0x50, 0x20, 0x20, 0x20, 0x20],
        'Z' | 'z' => [0xF8, 0x08, 0x10, 0x20, 0x40, 0x80, 0xF8],

        '0' => [0x70, 0x88, 0x98, 0xA8, 0xC8, 0x88, 0x70],
        '1' => [0x20, 0x60, 0x20, 0x20, 0x20, 0x20, 0x70],
        '2' => [0x70, 0x88, 0x08, 0x70, 0x80, 0x80, 0xF8],
        '3' => [0x70, 0x88, 0x08, 0x30, 0x08, 0x88, 0x70],
        '4' => [0x10, 0x30, 0x50, 0x90, 0xF8, 0x10, 0x10],
        '5' => [0xF8, 0x80, 0xF0, 0x08, 0x08, 0x88, 0x70],
        '6' => [0x38, 0x40, 0x80, 0xF0, 0x88, 0x88, 0x70],
        '7' => [0xF8, 0x08, 0x10, 0x20, 0x40, 0x40, 0x40],
        '8' => [0x70, 0x88, 0x88, 0x70, 0x88, 0x88, 0x70],
        '9' => [0x70, 0x88, 0x88, 0x78, 0x08, 0x10, 0xE0],

        ' ' => [0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00],
        '.' => [0x00, 0x00, 0x00, 0x00, 0x00, 0x20, 0x20],
        ',' => [0x00, 0x00, 0x00, 0x00, 0x00, 0x20, 0x40],
        '!' => [0x20, 0x20, 0x20, 0x20, 0x00, 0x20, 0x00],
        '?' => [0x70, 0x88, 0x08, 0x30, 0x20, 0x00, 0x20],
        '-' => [0x00, 0x00, 0x00, 0x70, 0x00, 0x00, 0x00],
        '_' => [0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xF8],
        ':' => [0x00, 0x20, 0x00, 0x00, 0x20, 0x00, 0x00],

        _   => [0xF8, 0x88, 0x88, 0x88, 0x88, 0x88, 0xF8], // Unknown char block
    };

    let mut result = [false; 35];
    for (y, row) in bitmap.iter().enumerate() {
        for x in 0..5 {
            // The font data is stored in the upper 5 bits of the byte (e.g. 0x80 is the leftmost bit)
            result[y * 5 + x] = (row & (0x80 >> x)) != 0;
        }
    }
    result
}
